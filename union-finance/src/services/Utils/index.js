export const shortenAddress = (address = '') => {
  if (address.length < 11) {
    return address;
  }
  return `${address.slice(0, 6)}...${address.slice(-4)}`;
};

export const formatNumber = (number, fixed) => {
  return Number(number).toFixed(fixed);
};
